#include <iostream>

using namespace std;

int main(int argc, char** argv) {

int number1, number2;

cout << "Enter first number: ";
cin >> number1;
cout << endl << "End second number: ";
cin >> number2;

int sum = number1 + number2;
cout << endl << "Sum of the two numbers is: " << sum;

int diff = number1 - number2;
cout << endl << "Difference of the two numbers is: " << diff;

int prod = number1 * number2;
cout << endl << "Product of the two numbers is: " << prod; 

int quot = number1 / number2;
cout << endl << "Quotient of the two numbers is: " << quot;

	
}
